"""
utils.py - Simple calculator

Includes simple functions:
-add
-subtract
-multiply
-divide
"""


def add(a: int, b: int) -> int:
    """Returns the sum of two integers a and b"""
    return a + b


def subtract(a: int, b: int) -> int:
    """Returns the result of subtracting integer b from integer a"""
    return a - b


def multiply(a: int, b: int) -> int:
    """Returns the product of two integers a and b"""
    return a * b


def divide(a: int, b: int) -> float:
    """Returns the result of dividing two integers a and b"""
    return a / b
